##Problem Statement: Create a function that takes a list of numbers. Return the largest number in the list.
##Problem Link: https://edabit.com/challenge/A7hyDnb72prWryeuY

#Examples:
#findLargestNum([4, 5, 1, 3]) ➞ 5
#findLargestNum([300, 200, 600, 150]) ➞ 600
#findLargestNum([1000, 1001, 857, 1]) ➞ 1001

#Solution:
def findLargestNum(number):
    
    #Return largest number in list
    return max(number)
